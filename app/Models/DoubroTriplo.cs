using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace app.Models
{
    public class DoubroTriplo
    {

        public int Doubro(int num1)
        {
            return num1 * 2;
        }


        public int Triplo(int num1)
        {
            return num1 * 3;
        }


        //pode retorna double com casas decimal
         public double Raiz(int num1)
        {
            return Math.Sqrt(num1);
        }





    }
}